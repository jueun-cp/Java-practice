package prob01;

public class ArithmeticOperator {
	//산술 연산자
	public static void main(String[] args) {
		System.out.println(20 + 3);
		System.out.println(20 - 3);
		System.out.println(20 * 3);
		//몫
		System.out.println(20 / 3);
		//나머지
		System.out.println(20 % 3);
		System.out.println(3 + 2 * 4);
		//괄호를 통해 우선순위를 높여줄 수 있음
		System.out.println((3 + 2) * 4 );
	}
}
